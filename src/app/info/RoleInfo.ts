export class RoleInfo {
    name: string;
    motto: string;
    text1: string;
    text2: string;
    text3: string;

    constructor(name: string, motto: string,
                text1: string, text2: string, text3: string) {
        this.name = name;
        this.motto = motto;
        this.text1 = text1;
        this.text2 = text2;
        this.text3 = text3;
    }
}
